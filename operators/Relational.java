public class Relational{
    public static void main(String [] args){
        
        int a=10;
        int b=20;

        System.out.println("10==20: " + (a==b));//false
        System.out.println("10!=20: " + (a!=b));//true
        System.out.println("10>20: " + (a>b));//false
        System.out.println("10<20: " + (a<b));//true
        System.out.println("10>=20: " + (a>=b));//false
        System.out.println("10<=20: " + (a<=b));//true
    }
}