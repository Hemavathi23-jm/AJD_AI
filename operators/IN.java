public class IN{
    public static void main(String [] args){

        //Post Incrememnt
        int num1 = 10;
        int res = num1++;
        System.out.println("num1:" +num1);
        System.out.println("res:" +res);
        //Pre Increment
        int a = 5;
        int b = ++a;
        System.out.println("a:" +a);
        System.out.println("b:" +b);
    }
}