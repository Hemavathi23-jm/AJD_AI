public class BultiIn {
    public static void main(String [] args){
        String s1 ="java";
        System.out.println("String length is: " + s1.length());//4
        System.out.println("length:"+"Java".length());//4
        System.out.println("character:"+"String".charAt(3));//1
        System.out.println("substring:"+"Programming".substring(0,7));//Program
        System.out.println("uppercase:"+"java".toUpperCase());//JAVA
        System.out.println("lowercase:"+"JAVA".toLowerCase());//java
        System.out.println("Contains:"+"Java".contains("va"));//true
        System.out.println("Replace:"+"Java".replace("java","javaScript"));//JavaScript

}
}